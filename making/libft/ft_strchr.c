#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*tmp;
	int		i;

	i = 0;
	tmp = (char *)s;
	while (tmp[i] && tmp[i] != c)
		i++;
	if (tmp[i] == 0 && c != 0)
		return (0);
	return (&tmp[i]);
}
